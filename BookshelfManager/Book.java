public class Book {
   private String id;
   private String title;
   private String author;
   private String note;
//Constructor
public Book(String id, String title, String author, String note){
    this.id = id;
    this.title = title;
    this.author = author;
    this.note = note;
}
//Getters
public String getId(){;
    return id;

}
public String getTitle(){;
    return title;

}
public String getAuthor(){;
    return author;

}
public String getNote(){;
    return note;

}
}
